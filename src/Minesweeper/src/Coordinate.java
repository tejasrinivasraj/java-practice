class Coordinate {
    private int xCoordinate;
    private int yCoordinate;
    Coordinate(int xCoordinate, int yCoordinate) {
        this.xCoordinate = xCoordinate;
        this.yCoordinate = yCoordinate;
    }

    int getCoordinateX() {
        return this.xCoordinate;
    }
    int getCoordinateY() {
        return this.yCoordinate;
    }
}
